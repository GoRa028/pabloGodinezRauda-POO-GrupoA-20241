public class Rectangle {
    int base;
    int height;

    public Rectangle(int b, int h) {
        base = b;
        height = h;
    }

    public int getArea() {
        int a;
        return a=base*height;
    }

    public int getPerimeter(){
        int p;
        int s=base+height;
        return p=2*s;
    }
}
